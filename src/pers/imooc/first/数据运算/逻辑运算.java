public class 逻辑运算 {
	public static void main(String[] args){
		boolean a=true;
		boolean b=false;
		boolean c=true;

		System.out.println("a^b的异或运算结果是: " + (a^b));
		System.out.println("a^c的异或运算结果是: " + (a^c));

		/**
		 * 异或运算:有且只有一个为true,结果才为true
		 */
	}

}
